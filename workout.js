function append(dat){
document.getElementById("workoutappend").innerHTML=null;

//container.innerHTML=null
dat.forEach(function(el){
    let div=document.createElement("div")

    let img=document.createElement("img")
    
    img.src=el.img_src

    let name=document.createElement("h2")

    name.innerText=el.Name


    div.append(img,name)

    div.setAttribute("class","boxmamta");

    

    var add = {
        img:el.img_src,
        name: el.Name,


        Frequency:el.Frequency,
        Category: el.Category,
        Exp_Level: el.Exp_Level,
        Views_Downloads: el.Views_Downloads,
        created_by: el.created_by,
        EliteFree: el.EliteFree,
        vipicon: el.vipicon
        
      }

      div.onclick = () => {
        addtonext(add);
      };

      document.querySelector("#workoutappend").append(div);
 
   
})

}

